class Solution(object):
    def uniquePathsWithObstacles(self, obstacleGrid):
        """
        :type obstacleGrid: List[List[int]]
        :rtype: int
        """
        # dp
        m = len(obstacleGrid)
        n = len(obstacleGrid[0])
        dp = [[0] * n for _ in range(m)]
        # dp func
        # dp[i][j] = dp[i-1][j] + dp[i][j-1]
        # init obstacle
        print(dp)
        print(n)
        print(dp[0])
        dp[0][2] = 4
        print(dp)
        for i in range(n):
            dp[0][i] = 2
        print(dp)

        flag = False
        for i in range(n):
            if obstacleGrid[0][i] == 1:
                flag = True
            if flag == False:
                dp[0][i] = 1
            else:
                dp[0][i] = 0
        print(dp)  # false
        flag = False
        for i in range(m):
            if obstacleGrid[i][0] == 1:
                flag = True
            if flag == False:
                dp[i][0] = 1
            else:
                dp[i][0] = 0
        # print(dp)

        # 二维动态规划怎么遍历
        for i in range(1, m):
            for j in range(1, n):
                if obstacleGrid[i][j] == 1:
                    dp[i][j] = 0
                else:
                    dp[i][j] = dp[i - 1][j] + dp[i][j - 1]
        # check
        print(dp)

        return dp[m - 1][n - 1]


